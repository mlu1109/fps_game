/* Primarily used for testing */

std::shared_ptr<VertexArray> newCubeIdx() // With indices
{
    const GLfloat n = 0.57735026919;
    const std::vector<Vertex> vertices{
        // front
        {{-0.5, -0.5, +0.5}, {-n, -n, +n}, {}, {0.0, 0.0}}, // 0
        {{+0.5, -0.5, +0.5}, {+n, -n, +n}, {}, {1.0, 0.0}}, // 1
        {{+0.5, +0.5, +0.5}, {+n, +n, +n}, {}, {1.0, 1.0}}, // 2
        {{-0.5, +0.5, +0.5}, {-n, +n, +n}, {}, {0.0, 1.0}}, // 3
        // back
        {{-0.5, -0.5, -0.5}, {-n, -n, -n}, {}, {0.0, 0.0}}, // 4
        {{+0.5, -0.5, -0.5}, {+n, -n, -n}, {}, {1.0, 0.0}}, // 5
        {{+0.5, +0.5, -0.5}, {+n, +n, -n}, {}, {1.0, 1.0}}, // 6
        {{-0.5, +0.5, -0.5}, {-n, +n, -n}, {}, {0.0, 1.0}}, // 7
    };
    const std::vector<GLuint> indices{ // texCoords do not match with every face
        // front
        0, 1, 2,
        2, 3, 0,
        // top
        1, 5, 6,
        6, 2, 1,
        // back
        7, 6, 5,
        5, 4, 7,
        // bottom
        4, 0, 3,
        3, 7, 4,
        // left
        4, 5, 1,
        1, 0, 4,
        // right
        3, 2, 6,
        6, 7, 3};

    return std::make_shared<VertexArray>(vertices, indices);
}

